    private void loadDataToTable() {
        List<ViewNhaSanXuat> list = service.findAll();
        String[] columns = {"Mã", "Tên"};
        dtm.setColumnIdentifiers(columns);
        tbHienThi.setModel(dtm);
        dtm.setRowCount(0);
        for (ViewNhaSanXuat view : list) {
            dtm.addRow(view.toDaTaRow());
        }
    }

    private ViewNhaSanXuat getProductFromInput() { // lấy đối tượng chức vụ từ input
        ViewNhaSanXuat x = new ViewNhaSanXuat();
        String ma = txtMa.getText();
        String ten = txtTen.getText();
        x.setMa(ma);
        x.setTen(ten);
        return x;
    }

    public void fill(int index) {
        ViewNhaSanXuat view = service.findAll().get(index);
        txtMa.setText(view.getMa());
        txtTen.setText(view.getTen());
    }

