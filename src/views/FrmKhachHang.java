    private void loadDataToTable() {
        List<ViewKhachHang> list = khachHangService.findAll();
        String[] columns = {"Mã", "Tên", "Tên Đệm", "Họ", "Ngày Sinh", "Sdt", "Địa chỉ", "Thành phố", "Quốc gia", "Mật khẩu"};
        dtm.setColumnIdentifiers(columns);
        tbHienThi.setModel(dtm);
        dtm.setRowCount(0);
        for (ViewKhachHang viewKhachHang : list) {
            dtm.addRow(viewKhachHang.toDataRow());
        }
    }

    private ViewKhachHang getProductFromInput() { // lấy đối tượng chức vụ từ input
        ViewKhachHang khachHang = new ViewKhachHang();
        String ma = txtMa.getText();
        String ten = txtTen.getText();
        String tenDem = txtTenDem.getText();
        String ho = txtHo.getText();
        String ngaySinh = txtNgaySinh.getText();
        String sdt = txtSdt.getText();
        String diaChi = txtDiaChi.getText();
        String thanhPho = txtThanhPho.getText();
        String quocGia = txtQuocGia.getText();
        String matKhau = txtMatKhau.getText();

        khachHang.setMa(ma);
        khachHang.setTen(ten);
        khachHang.setTenDem(tenDem);
        khachHang.setHo(ho);
        khachHang.setNgaySinh(ngaySinh);
        khachHang.setSdt(sdt);
        khachHang.setDiaChi(diaChi);
        khachHang.setThanhPho(thanhPho);
        khachHang.setQuocGia(quocGia);
        khachHang.setMatKhau(matKhau);

        return khachHang;
    }

    public void fill(int index) {
        ViewKhachHang vkh = khachHangService.findAll().get(index);
        txtDiaChi.setText(vkh.getDiaChi());
        txtThanhPho.setText(vkh.getThanhPho());
        txtQuocGia.setText(vkh.getQuocGia());
        txtMatKhau.setText(vkh.getMatKhau());
        txtMa.setText(vkh.getMa());
        txtTen.setText(vkh.getTen());
        txtTenDem.setText(vkh.getTenDem());
        txtHo.setText(vkh.getHo());
        txtSdt.setText(vkh.getSdt());
        txtNgaySinh.setText(vkh.getNgaySinh());
    }

